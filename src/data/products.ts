// Fonctions utilitaires adaptées aux tableaux de marques
export const getProductsByBrand = (brand: string): Product[] => {
  return products.filter((product) => product.brand.includes(brand))
}

export const getBrands = (): string[] => {
  const allBrands = products.flatMap((product) => product.brand)
  return [...new Set(allBrands)].sort()
}

export const searchProducts = (query: string): Product[] => {
  const searchTerm = query.toLowerCase()
  return products.filter(
    (product) =>
      product.name.toLowerCase().includes(searchTerm) ||
      product.description.toLowerCase().includes(searchTerm) ||
      product.brand.some((brand) => brand.toLowerCase().includes(searchTerm)),
  )
}
